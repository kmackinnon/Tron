DROP TABLE IF EXISTS Maps;
DROP TABLE IF EXISTS GameStats;
DROP TABLE IF EXISTS UserStats;
DROP TABLE IF EXISTS Users;
CREATE TABLE Users (id INTEGER PRIMARY KEY AUTOINCREMENT, username VARCHAR(255) UNIQUE NOT NULL, password CHAR(512) NOT NULL, salt CHAR(255) NOT NULL);
CREATE TABLE UserStats (user_id INTEGER PRIMARY KEY NOT NULL, wins INTEGER, losses INTEGER, games INTEGER, FOREIGN KEY(user_id) REFERENCES Users(id));
CREATE TABLE GameStats (id INTEGER PRIMARY KEY AUTOINCREMENT, player_one INTEGER NOT NULL, player_two INTEGER NOT NULL, winner INTEGER NOT NULL, player_one_rounds INTEGER NOT NULL, player_two_rounds INTEGER NOT NULL, FOREIGN KEY(player_one) REFERENCES Users(id),  FOREIGN KEY(player_two) REFERENCES Users(id));
CREATE TABLE Maps (id INTEGER PRIMARY KEY AUTOINCREMENT, map_name VARCHAR(255) NOT NULL, width Integer, height INTEGER, map_data TEXT NOT NULL);
